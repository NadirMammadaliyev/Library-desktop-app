    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            GiveBook giveBook = new GiveBook();

            ReadingRoom readingRoom = (ReadingRoom) readingRoomCmb.getSelectedItem();
            Employee employee = (Employee) employeeCmb.getSelectedItem();
            Book book = (Book) bookCmb.getSelectedItem();
            User user = (User) userCmb.getSelectedItem();
            Date date = returnDateTxt.getDate();

            giveBook.setBook(book);
            giveBook.setUser(user);
            giveBook.setEmployee(employee);
            giveBook.setReadingRoom(readingRoom);
            giveBook.setRetrunDate(date);

            if (giveBook.getBook().getNumberOfBook() > 0) {
                giveBookService.addGiveBook(giveBook);
                Integer numOfBook = bookService.getNumberOfBook(book.getId());
                bookService.updateNumberOfBookMinus(book, numOfBook);
                Integer userActive = userService.getUserActivity(user.getId());
                userService.updateUserActivity(user, userActive);
                JOptionPane.showMessageDialog(null, "Infomation has been succssessfully added!");

            } else if(giveBook.getBook().getNumberOfBook() <= 0) {
                JOptionPane.showMessageDialog(null, "'" + giveBook.getBook().getName() + "'" + " A number of book equals 0");
            }
          
            this.dispose();

        } catch (Exception ex) {
            ex.printStackTrace();

        }
    }//GEN-LAST:event_saveBtnActionPerformed

