    private void showEmployeeCombo() {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) employeeCmb.getModel();
            List<Employee> employeeList = empoyeeService.getEmployeeList();
            for (Employee employee : employeeList) {
                comboBoxModel.addElement(employee);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showReadingBookCombo() {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) readingRoomCmb.getModel();
            List<ReadingRoom> readingRoomList = readingRoomService.getReadingRoomList();
            for (ReadingRoom readingRoom : readingRoomList) {
                comboBoxModel.addElement(readingRoom);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

