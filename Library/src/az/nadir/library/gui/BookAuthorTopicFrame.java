    private void showAuthorData(List<Author> authorList) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) authorCmb.getModel();

            for (Author author : authorList) {
                comboBoxModel.addElement(author);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showTopicData() {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) topicCmb.getModel();
            List<Topic> topicList = topicService.getTopicList();
            for (Topic topic : topicList) {
                comboBoxModel.addElement(topic);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showBookData(List<Book> bookList) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) bookCmb.getModel();

            for (Book book : bookList) {
                comboBoxModel.addElement(book);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
