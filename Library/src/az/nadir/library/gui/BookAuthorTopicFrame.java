    private void searchLblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchLblMouseClicked
        BookInfo bookInfo = new BookInfo();
        Topic topic = null;
        try {
            Author author = (Author) authorCmb.getSelectedItem();
            Book book = (Book) bookCmb.getSelectedItem();

            topic = (Topic) topicCmb.getSelectedItem();

            bookInfo.setBook(book);
            bookInfo.setAuthor(author);
            bookInfo.setTopic(topic);
            bookInfoService.addBookInfo(bookInfo);
            JOptionPane.showMessageDialog(null, "Succsessfulyy added!");
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_searchLblMouseClicked

