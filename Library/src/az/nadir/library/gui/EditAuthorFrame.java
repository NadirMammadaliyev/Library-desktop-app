    private void showOldAuthorData() {
        try {
            Author author = authorService.getAuthorById(selectedId);
            nameTxt.setText(author.getName());
            surnameTxt.setText(author.getSurname());
        } catch (Exception ex) {
            Logger.getLogger(EditAuthorFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
