    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            GiveBook giveBook = giveBookService.getGiveBookById(selectedId);

            ReadingRoom readingRoom = (ReadingRoom) readingRoomCmb.getSelectedItem();
            Employee employee = (Employee) employeeCmb.getSelectedItem();
            Book book = (Book) bookCmb.getSelectedItem();
            User user = (User) userCmb.getSelectedItem();
            Date date = returnDateTxt.getDate();

            giveBook.setBook(book);
            giveBook.setUser(user);
            giveBook.setEmployee(employee);
            giveBook.setReadingRoom(readingRoom);
            giveBook.setRetrunDate(date);
            giveBookService.updateGiveBook(giveBook);

            JOptionPane.showMessageDialog(null, "Infomation has been succssessfully updated!");
            this.dispose();

        } catch (Exception ex) {
            ex.printStackTrace();

        }
    }//GEN-LAST:event_saveBtnActionPerformed

