    private void showOldGiveBookData() {
        try {
            GiveBook giveBook = giveBookService.getGiveBookById(selectedId);
            returnDateTxt.setDate(giveBook.getRetrunDate());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showEmployeeCombo(Employee e) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) employeeCmb.getModel();
            List<Employee> employeeList = empoyeeService.getEmployeeList();
            for (Employee employee : employeeList) {
                comboBoxModel.addElement(employee);
            }
            comboBoxModel.setSelectedItem(e);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showBookData(List<Book> bookList) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) bookCmb.getModel();
            for (Book book : bookList) {
                comboBoxModel.addElement(book);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showUserData(List<User> userList) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) userCmb.getModel();
            for (User user : userList) {
                comboBoxModel.addElement(user);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showBookCombo(Book b) {
        try {
           
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) bookCmb.getModel();
            Book b1 = bookService.getBookById(b.getId());
            b.setLanguage(b1.getLanguage());
            comboBoxModel.addElement(b);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showUserCombo(User u) {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) userCmb.getModel();
            comboBoxModel.addElement(u);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void showReadingRoom(ReadingRoom r) {
         try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) readingRoomCmb.getModel();
            List<ReadingRoom> readingRoomList = readingRoomService.getReadingRoomList();
            for (ReadingRoom readingRoom : readingRoomList) {
                comboBoxModel.addElement(readingRoom);
            }
            comboBoxModel.setSelectedItem(r);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

  
