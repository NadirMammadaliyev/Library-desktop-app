    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            BookInfo bookInfo = new BookInfo();
            Book book = new Book();
            String name = nameTxt.getText();
            Language language = (Language) languageCmb.getSelectedItem();
            Integer page = Integer.parseInt(pageTxt.getText());
            Shelf shelf = (Shelf) shelfCmb.getSelectedItem();
            Double price = Double.parseDouble(priceTxt.getText());
            Integer numOfBook = Integer.parseInt(numberOfBookTxt.getText());
            String dop = dateOfPubTxt.getText();
            
            book.setName(name);
            book.setLanguage(language);
            book.setPage(page);
            book.setShelf(shelf);
            book.setPrice(price);
            book.setDateOfPublication(dop);
            book.setNumberOfBook(numOfBook);
            
            bookInfo.setBook(book);
            bookService.addBook(bookInfo);
            JOptionPane.showMessageDialog(null, "Book has been succsessfully added");
            this.dispose();
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        

    }//GEN-LAST:event_saveBtnActionPerformed

