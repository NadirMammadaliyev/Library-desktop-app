    private void showLanguageCombo() {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) languageCmb.getModel();
            List<Language> languageList = languageService.getLanguageList();
            for (Language language : languageList) {
                comboBoxModel.addElement(language);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void showShelfCombo() {
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) shelfCmb.getModel();
            List<Shelf> shelfList = shelfService.getShelfList();
            for (Shelf shelf : shelfList) {
                comboBoxModel.addElement(shelf);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
