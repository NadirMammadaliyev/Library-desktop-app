    private void showOldBookData() {
        try {
            Book book =bookService.getBookById(selectedId);
            nameTxt.setText(book.getName());
            pageTxt.setText(String.valueOf(book.getPage()));
            priceTxt.setText(String.valueOf(book.getPrice()));
            numberOfBookTxt.setText(String.valueOf(book.getNumberOfBook()));
            dateOfPubTxt.setText(book.getDateOfPublication());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    private void showLanguageCombo(Language l){
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) languageCmb.getModel();
            List<Language> languageList = languageService.getLanguageList();
            for (Language language : languageList) {
                comboBoxModel.addElement(language);
            }
            comboBoxModel.setSelectedItem(l);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
     private void showShelfCombo(Shelf s){
        try {
            DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) shelfCmb.getModel();
            List<Shelf> shelfList = shelfService.getShelfList();
            for (Shelf shelf : shelfList) {
                comboBoxModel.addElement(shelf);
            }
            comboBoxModel.setSelectedItem(s);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
