    private void login() {
           String username = usernameTxt.getText();
        String password = passwordTxt.getText();

        try {
            if (username != null && !username.isEmpty() && passwordTxt != null && !password.isEmpty()) {
                LoginUser loginUser = loginUserService.login(username, password);
                if (loginUser != null) {
                    MainFrame mainFrame = new MainFrame(userService, languageService, authorService, employeeService, topicService, positionService, bookService, shelfService, bookInfoService, readingRoomService, giveBookService,loginUser);
                    mainFrame.setLocationRelativeTo(null);
                    mainFrame.setTitle("Library");
                //    mainFrame.setExtendedState(MAXIMIZED_BOTH);
                    mainFrame.setVisible(true);

                    this.dispose();
                } else {
                    errorMsg.setVisible(true);
                    errorMsg.setText("Username or password is invalid!");
                }
            } else {
                errorMsg.setVisible(true);
                errorMsg.setText("Username or password is empty!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
